    private void CalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculateActionPerformed
        // TODO add your handling code here:
        int vlr1,vlr2,vlr3;
        int Cant1,Cant2,Cant3;     
        int VlrTol1,VlrTol2,VlrTol3;
        int ValorTotal = 0;

        vlr1 = Integer.parseInt(FstCostObj.getText());
        vlr2 = Integer.parseInt(SndCostObj.getText());
        vlr3 = Integer.parseInt(TrCostObj.getText());
        Cant1 = Integer.parseInt(FstAmot.getText());
        Cant2 = Integer.parseInt(SndAmot.getText());
        Cant3 = Integer.parseInt(TrAmot.getText());
        VlrTol1 = vlr1*Cant1;
        VlrTol2 = vlr2*Cant2;
        VlrTol3 = vlr3*Cant3;
        ValorTotal = VlrTol1+VlrTol2+VlrTol3;
        FstCost.setText(""+VlrTol1);
        SndCost.setText(""+VlrTol2);
        TrCost.setText(""+VlrTol3); 
        FinalText.setText("Se√±or(a): "+ClientName.getText()+" el total de su compra es: "+ ValorTotal);
        TotalCost.setText(""+ValorTotal);
    }//GEN-LAST:event_CalculateActionPerformed

